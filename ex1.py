INDICE = 13    #Declaração das variáveis.
SOMA = 0
K = 0

while K < INDICE:  #Aplicando estrutura de repetição para K (0) menor que indice (13)
    K = K + 1          #Enquanto K for menor que índice, K recebe 1
    SOMA = SOMA + K    #K é armazenado dentro de soma a cada vez que a condição se repete

print("A soma é igual a: ",SOMA) #print de soma a partir do momento que a condição de repetição foi suprida